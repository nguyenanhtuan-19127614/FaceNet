import UIKit

extension UIColor {
    
    class func hcmusColor() -> UIColor {
        return UIColor(red: 34/255, green: 60/255, blue: 117/255, alpha: 1.0)
    }
    
    class func hcmusLightColor() -> UIColor {
        return UIColor(red: 206/255, green: 211/255, blue: 223/255, alpha: 1.0)
    }
    
}
